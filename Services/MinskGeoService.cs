    /// <summary>
    /// Определить район Минска по GPS-координатам.
    /// </summary>
    /// <param name="lat">Широта</param>
    /// <param name="lon">Долгота</param>
    /// <returns>Название района или null, если точка вне Минска</returns>
    public string? GetDistrictByCoords(double lat, double lon)
    {
        // Проверяем каждый полигон
        foreach (var (district, polygon) in _districtPolygons)
        {
            if (IsPointInPolygon(lat, lon, polygon))
            {
                return district;
            }
        }
        return null;
    }

    /// <summary>
    /// Определить район по названию микрорайона в адресе.
    /// </summary>
    public string? GetDistrictByAddress(string address)
    {
        if (string.IsNullOrWhiteSpace(address))
            return null;

        foreach (var (micro, district) in _microdistrictMap)
        {
            if (address.Contains(micro, StringComparison.OrdinalIgnoreCase))
            {
                return district;
            }
        }
        return null;
    }

    /// <summary>
    /// Ray Casting Algorithm — определяет, находится ли точка внутри полигона.
    /// Работает для любых (в т.ч. невыпуклых) полигонов.
    /// </summary>
    private static bool IsPointInPolygon(double lat, double lon, (double Lat, double Lon)[] polygon)
    {
        bool inside = false;
        int n = polygon.Length;

        for (int i = 0, j = n - 1; i < n; j = i++)
        {
            double yi = polygon[i].Lat, xi = polygon[i].Lon;
            double yj = polygon[j].Lat, xj = polygon[j].Lon;

            bool intersects = ((yi > lat) != (yj > lat))
                && (lon < (xj - xi) * (lat - yi) / (yj - yi) + xi);

            if (intersects)
                inside = !inside;
        }

        return inside;
    }

    private const double MinskCenterLat = 53.9006;
    private const double MinskCenterLon = 27.5590;

    public double GetDistanceToMinskCenter(double lat, double lon)
    {
        return CalculateHaversineDistance(lat, lon, MinskCenterLat, MinskCenterLon);
    }

    public static double CalculateHaversineDistance(double lat1, double lon1, double lat2, double lon2)
    {
        const double R = 6371.0;

        double lat1Rad = lat1 * Math.PI / 180.0;
        double lat2Rad = lat2 * Math.PI / 180.0;
        double deltaLat = (lat2 - lat1) * Math.PI / 180.0;
        double deltaLon = (lon2 - lon1) * Math.PI / 180.0;

        double a = Math.Sin(deltaLat / 2) * Math.Sin(deltaLat / 2) +
                   Math.Cos(lat1Rad) * Math.Cos(lat2Rad) *
                   Math.Sin(deltaLon / 2) * Math.Sin(deltaLon / 2);

        double c = 2 * Math.Atan2(Math.Sqrt(a), Math.Sqrt(1 - a));

        return R * c;
    }
